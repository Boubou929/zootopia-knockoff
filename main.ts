controller.B.onEvent(ControllerButtonEvent.Pressed, function () {
    if (Enclosure == 3) {
        if (PlayerTurn == true) {
            CursorAt = curX + curY * 3
            if (CursorAt == CodeSequence[CurrentIndex]) {
                AddtoSpriteList()
                info.changeScoreBy(1)
                CurrentIndex += 1
                if (CurrentIndex == CodeSequence.length) {
                    NextLevel()
                }
            } else {
                info.changeLifeBy(-1)
                music.powerDown.play()
            }
        }
    }
})
