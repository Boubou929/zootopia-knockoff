function PlaySequence () {
    for (let index = 0; index <= CodeSequence.length - 1; index++) {
        CurrentIndex = index
        AddtoSpriteList()
        pause(200)
    }
    pause(600)
    for (let value of spriteList) {
        value.destroy()
    }
    CurrentIndex = 0
    PlayerTurn = true
}
